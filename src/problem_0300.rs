fn length_of_lis(nums: &[i32]) -> i32 {
    let mut dp: Vec<i32> = vec![];

    for &num in nums {
        if let Err(j) = dp.binary_search(&num) {
            if j == dp.len() {
                dp.push(num);
            } else {
                dp[j] = num;
            }
        }
    }

    dp.len() as i32
}

impl Solution {
    #[must_use]
    #[expect(clippy::needless_pass_by_value, reason = "External invocation")]
    pub fn length_of_lis(nums: Vec<i32>) -> i32 {
        length_of_lis(&nums)
    }
}

