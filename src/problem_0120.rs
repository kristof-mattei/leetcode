fn minimum_total(triangle: &[Vec<i32>]) -> i32 {
    let rows = triangle.len();

    let mut dp = triangle[rows - 1].clone();

    for i in (0..rows - 1).rev() {
        for j in 0..=i {
            dp[j] = triangle[i][j] + i32::min(dp[j], dp[j + 1]);
        }
    }
    dp[0]
}

impl Solution {
    #[must_use]
    #[expect(clippy::needless_pass_by_value, reason = "External invocation")]
    pub fn minimum_total(triangle: Vec<Vec<i32>>) -> i32 {
        minimum_total(&triangle)
    }
}

