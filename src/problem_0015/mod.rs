fn three_sum(mut nums: Vec<i32>) -> Vec<Vec<i32>> {
    if nums.len() < 3 {
        return vec![];
    }

    nums.sort_unstable();

    let mut tripples = Vec::<Vec<i32>>::new();

    for tripple_1_index in 0..nums.len() - 2 {
        // Skip all duplicates from left
        if tripple_1_index > 0 && nums[tripple_1_index] == nums[tripple_1_index - 1] {
            continue;
        }

        let mut tripple_2_index = tripple_1_index + 1;
        let mut tripple_3_index = nums.len() - 1;

        while tripple_2_index < tripple_3_index {
            let sum = nums[tripple_1_index] + nums[tripple_2_index] + nums[tripple_3_index];
            match sum {
                | 0 => {
                    tripples.push(vec![
                        nums[tripple_1_index],
                        nums[tripple_2_index],
                        nums[tripple_3_index],
                    ]);

                    tripple_3_index -= 1;

                    // Skip all duplicates from right
                    while tripple_2_index < tripple_3_index
                        && nums[tripple_3_index] == nums[tripple_3_index + 1]
                    {
                        tripple_3_index -= 1;
                    }
                },
                | _ if sum > 0 => {
                    // Decrement to reduce sum value (we're sorted)
                    tripple_3_index -= 1;
                },
                | _ => {
                    // Increment to increase sum value
                    tripple_2_index += 1;
                },
            }
        }
    }
    tripples
}

impl Solution {
    #[must_use]
    #[allow(clippy::needless_pass_by_value)]
    pub fn three_sum(nums: Vec<i32>) -> Vec<Vec<i32>> {
        three_sum(nums)
    }
}

